    private void BotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarActionPerformed
        
       int rango, validacion=0;
       String  NombreUsuario, contrasena, nombredeperfil, edades, nivel="";
       
       
       NombreUsuario = nombreusuario.getText().trim();
       contrasena = password.getText().trim();
       nombredeperfil = NombrePerfil.getText().trim();
       edades = edad.getText().trim();
       rango = RangoDeAcceso.getSelectedIndex() + 1;
       
       if(NombreUsuario.equals("")){
           nombreusuario.setBackground(Color.red);
           validacion++;
       }
       
       if(contrasena.equals("")){
           password.setBackground(Color.red);
           validacion++;
       }
       
       if(nombredeperfil.equals("")){
           NombrePerfil.setBackground(Color.red);
           validacion++;
       }
       
       if(edades.equals("")){
           edad.setBackground(Color.red);
           validacion++;
       }
       
       if(rango == 1){
           nivel = "Lider";
       }else if(rango == 2){
           nivel = "Desarrollador";
       }
       
       try{
           Connection cn = ConexionBaDa.conectar();
           PreparedStatement pst = cn.prepareStatement("select usuario from desarrolladores where usuario = '" + contrasena + "'");
           ResultSet rs = pst.executeQuery();
           
           if(rs.next()){
               nombreusuario.setBackground(Color.red);
               JOptionPane.showMessageDialog(null, "Nombre de usuario no disponible");
               cn.close();
           } else{
               
               cn.close();
               if(validacion == 0){
                   try{
                       Connection con = ConexionBaDa.conectar();
                       PreparedStatement pst2 = con.prepareStatement("insert into desarrolladores values (?,?,?,?,?,?,?)");
                       pst2.setInt(1, 0);
                       pst2.setString(2, NombreUsuario);
                       pst2.setString(3, contrasena);
                       pst2.setString(4, nombredeperfil);
                       pst2.setString(5, edades);
                       pst2.setString(6, nivel);
                       pst2.setInt(7, 0);
                       pst2.executeUpdate();
                       con.close();
                       
                       Limpiar();
                       JOptionPane.showMessageDialog(null, "Registro exitoso");
                       this.dispose();
                   } catch(SQLException e){
                       System.err.println("surgio un inconveniente" + e);
                   }
               }else{
                   JOptionPane.showMessageDialog(null, "revisa tus Datos");
               }
               
           }
       } catch(SQLException e){
           System.err.println("no se pudo confirmar tu usuario" + e);
           JOptionPane.showMessageDialog(null, "Error al comparar usuario");
       }
    }//GEN-LAST:event_BotonGuardarActionPerformed
    
